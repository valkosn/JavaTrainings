public class Mouse
{
    int x;
    int y;

    public int getX()
    {
        return x;
    }

    public void setX(int x)
    {
        this.x = x;
    }

    public int getY()
    {
        return y;
    }

    public void setY(int y)
    {
        this.y = y;
    }

    public Mouse(int x, int y)
    {

        this.x = x;
        this.y = y;
    }
}
